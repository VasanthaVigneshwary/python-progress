# Day 3 - Data Types in Python

# Integer
age = 20
print("Age:", age)
print(type(age))

# Float
cgpa = 8.92
print("\nCGPA:", cgpa)
print(type(cgpa))

# String
name = "Varsha"
print("\nName:", name)
print(type(name))

# Boolean
is_student = True
print("\nIs Student:", is_student)
print(type(is_student))

# Student Information
student_name = "Varsha"
student_age = 20
student_cgpa = 8.9
hosteller = False

print("\nStudent Information")
print(student_name)
print(student_age)
print(student_cgpa)
print(hosteller)

# Difference between String and Integer

a = "100"
b = "200"
print("\nString Addition:", a + b)

x = 100
y = 200
print("Integer Addition:", x + y)

# Online Shopping Example
product = "Laptop"
price = 65000.50
quantity = 2
available = True

print("\nShopping Details")
print(product)
print(price)
